/**
 * Get celebrities whose death date matches a given month & day
 */
export function getStaticTodayInHistory(month: number, day: number): StaticCelebrity[] {
  return STATIC_CELEBRITIES.filter(
    (c) => c.death_month === month && c.death_day === day
  );
}

/**
 * Fallback "On This Day" — if no exact match, show closest ± 3 days
 */
export function getStaticNearbyHistory(month: number, day: number): StaticCelebrity[] {
  const exact = getStaticTodayInHistory(month, day);
  if (exact.length > 0) return exact;

  // No exact match today — show a mix: 3 memorials + 1 celebration
  const memorials = STATIC_CELEBRITIES.filter((c) => c.is_featured && c.lifecycle_type === "memorial").slice(0, 3);
  const celebrations = STATIC_CELEBRITIES.filter((c) => c.is_featured && c.lifecycle_type !== "memorial").slice(0, 1);
  return [...memorials, ...celebrations];
}

